package dasturlash.uz.dto;

public class Word {
    private Integer id;
    private String engWord;
    private String uzbWord;


    public String getEngWord() {
        return engWord;
    }

    public void setEngWord(String engWord) {
        this.engWord = engWord;
    }

    public String getUzbWord() {
        return uzbWord;
    }

    public void setUzbWord(String uzbWord) {
        this.uzbWord = uzbWord;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

}
